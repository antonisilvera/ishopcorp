    function cambia(){
        var depa;
        depa = document.querySelector("#departamento")[document.querySelector("#departamento").selectedIndex].value;
        
        if(depa!=0){
            mis_opts=eval("opt_"+depa);
            num_opts=mis_opts.length;
            document.querySelector("#provincia").length = num_opts;
            for(i=0; i<num_opts; i++){
                document.querySelector("#provincia").options[i].value=(mis_opts[i]).replace(/ /g, "");
                document.querySelector("#provincia").options[i].text=mis_opts[i];
            }
        }else{
            document.querySelector("#provincia").length = 1;
            document.querySelector("#provincia").options[0].value="-";
            document.querySelector("#provincia").options[0].text="-";
        }
        //reseteando las opts
        document.querySelector("#provincia").options[0].selected = true;
    }

